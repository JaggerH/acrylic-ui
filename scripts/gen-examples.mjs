// Generates components/examples-map.ts from components/examples/*.tsx.
// The example index is the single source of truth for live previews — never edit
// examples-map.ts by hand; add an example file and rerun `pnpm examples:gen`.
import { readdirSync, writeFileSync } from "node:fs"

const dir = "components/examples"
const names = readdirSync(dir)
  .filter((f) => f.endsWith(".tsx"))
  .map((f) => f.replace(/\.tsx$/, ""))
  .sort()

const imports = names.map((n, i) => `import C${i} from "./examples/${n}"`).join("\n")
const entries = names.map((n, i) => `  "${n}": C${i},`).join("\n")

const out = `// AUTO-GENERATED by scripts/gen-examples.mjs — do not edit.
import type { ComponentType } from "react"
${imports}

export const examples: Record<string, ComponentType> = {
${entries}
}
`

writeFileSync("components/examples-map.ts", out)
console.log(`generated components/examples-map.ts (${names.length} examples)`)
